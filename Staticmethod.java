class count{

   static void calc(){     //static method
     System.out.println("Hello");

    }

}

public class Staticmethod {
    public static void main(String[] args) {
        count.calc();         //static methods can be accessed directly with help of class name, no need to create object.
    }
}

//A method in Java is a block of code that, when called, performs specific actions mentioned in it.
